class ShowForm(FlaskForm):
    artist_id = StringField(
        'artist_id', validators=[DataRequired()]
    )
    venue_id = StringField(
        'venue_id', validators=[DataRequired()]
    )
    start_date = DateTimeField(
        'start_date',
        validators=[DataRequired()],
        default= datetime.today()
    )

